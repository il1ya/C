/*Напишите программу, которая повторяет на экране аргументы командной строки в обратном порядке. 
Другими словами, если аргументами командной строки являются до скорого свидания, данная программа должна вывести на экран свидания скорого до.*/
#include <stdio.h>
//#include <string.h>
#define LIM 20
int main(int argc, char *argv[])
{
	int i;
	char *p[LIM];
	for(i = 0; i < argc; i++)
		p[i] = argv[i];
	p[i] = '\0';
	for(; i >= 0; --i)
		puts(p[i]);
	return 0;
}