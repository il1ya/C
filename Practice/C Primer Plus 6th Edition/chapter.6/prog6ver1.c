/*
Воспользуйтесь вложенными циклами для написания программы, которая выводит следующую последовательность символов:
$$$$$$$$
$$$$$$$$
$$$$$$$$
$$$$$$$$
*/
#include <stdio.h>
int main (void)
{
	int b, a;
	for (b = 1; b <= 4; b++)
	{
		for (a = 1; a <= 8; a++)
			printf("$");
			printf("\r\n");
	}
	return 0;
}