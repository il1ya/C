/*
Воспользуйтесь вложенными циклами для написания программы, которая выводит следующую последовательность символов:
$$$$$$$$
$$$$$$$$
$$$$$$$$
$$$$$$$$
*/
#include <stdio.h>
#define NI 36
int main (void)
{
	int b;
	for (b = NI; b <= 39; b++)
	{
		printf("%c%c%c%c%c%c%c%c", b, b, b, b, b, b, b, b);
		printf("\r\n");
	}
		return 0;
}