/*
Каким будет вывод каждого из показанных далее фрагментов для указанного ввода (предположите, что переменная ch имеет тип int и ввод является буферизированным)?
а. Ввод выглядит следующим образом:
If you quit, I will.[enter]
*/
#include <stdio.h>
int main(void)
{
	int ch;
	while ((ch = getchar()) != 'i')
		putchar(ch);
	return 0;
}