/* bases.c -- выводит число 100 в десятичной, восьмеричной и шестадцатеричной форме */
#include <stdio.h>
int main (void)
{
	int x = 100;
	printf("DEC = %d; OCT = %o; HEX = %x\r\n", x, x, x); // %o вывести целое число в восьмеричном OCT виде.
	// для отображения целого числа в шестнадцатеричном HEX виде служит спецификатор %x
	printf("DEC = %d; OCT = %#o; HEX = %#x\r\n", x, x, x); // Вывести на экран префиксы языка С, %#o, %#x
	return 0;
}